class IfElseBolte
{
    public static void main(String args[])
    {
        int x = 17;

        if (x <= 10)
        {
            System.out.print("Value of X is less than or equal to 10");
        }
        else if (x <= 20)
        {
            System.out.print("Value of X is less than or equal to 20");
        }
        else if (x <= 30)
        {
            System.out.print("Value of X is less than or equal to 30");
        }
        else
        {
            System.out.print("Out Of Range");
        }
    }
}